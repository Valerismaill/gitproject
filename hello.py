def add_numbers(a, b):
    """
    Эта функция принимает два числа и возвращает их сумму.
    Параметры:
    a (int): первое число
    b (int): второе число
    Возвращает:
    int: сумма двух чисел
    """
    return a + b  # Возвращаем сумму чисел

# Основной блок программы
if __name__ == "__main__":
    result = add_numbers(5, 3)  # Сложение 5 и 3
    print(f"Результат сложения: {result}")  # Вывод: 8